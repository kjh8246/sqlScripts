--문제 변영 : 매출이 없는 회원은 조회되지 않는다. "를 "매출이 없는 회원은 0으로 출력한다."
--			1) null값을 0으로 출력하는 함수 : nvl	2)외부조인으로 수정합니다.
--			3) 매출합계가 같을 때는 회원번호 순서로 출력합니다.

SELECT mt.CUSTNO , CUSTNAME , decode(GRADE,'A','VIP','B','일반','C','직원)' AS agrade,
		nvl(ASUM,0) AS "매출합계" FROM MEMBER_TBL_02 mt 
		(SELECT custno, sum(price) AS asum FROM mouney_tbl_02 mt
		GROUP BY CUSTNO 
		ORDER BY ASUM DESC ) sale
WHERE mt.CUSTNO = sale.custno(+);	--2)
ORDER BY asum DESC,custno;			--3) asum 이 같을 때는 custno 순서로 합니다.

